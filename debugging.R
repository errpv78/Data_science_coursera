# Debugging Tools in R
"traceback: prints out function call stck after an error occurs; does
nothing if there's no error
debug: flags function for 'debug' mode which allows to step through
execution of function one line at a time
browser: suspends execution of function wherever it is called and 
puts function in debug mode
trace: allows to insert debugging code into a function a
specific places
recover: allows to modify error behaviour so as to browse function
call stack
